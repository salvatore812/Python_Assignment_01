s = "'python programming'"
sStrip = s.strip()
print("Stripped:",sStrip)

sUpper = sStrip.upper()
print("Stripped + Upper:",sUpper)

sTitle = sStrip.title()
print("Stripped + Title:",sTitle)

sReplace = sStrip.replace('python','data')
print("Stripped + Replace:",sReplace)

sLength = len(sStrip)
print("The length of the stripped is:",sLength)





